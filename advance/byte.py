# +1 = 0000 0000 0000 0001
# -1 = 1000 0000 0000 0001
# -----------------------------------------
#      1000 0000 0000 0010
# 数字在计算机中，是用二进制补码的形式来保存的，因此-1 +1需要按照补码进行相加才是正确的结果

# 反码 = 符号位不变，其他位相反
# 补码 = 反码 + 1

# -1 原码: 1000 0000 0000 0001
# -1 反码： 1111 1111 1111 1110
# -1 补码： 1111 1111 1111 1111

# +1 = 0000 0000 0000 0001
# -1 = 1111 1111 1111 1111
# -----------------------------------------
#      0000 0000 0000 0000

# 原码 = 补码的符号位不变 -->数据位取反--> 尾+1
# 1111 1111 1111 1111 => 1000 0000 0000 0000 => 1000 0000 0000 0001


